package september25;

public class SwitchCase {
    public static void main(String[] args) {
        int num = 20;

        switch (num) {
            case 10:
                System.out.println("Number is 1");
                break;
            case 20:
                System.out.println("Number is 2");
               break;
            case 30:
                System.out.println("Number is 3");
                break;
            default:
                System.out.println("Number is not found");
        }
    }
}
